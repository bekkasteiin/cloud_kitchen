class ShopItemModel {
  List<CategoryList> category;

  ShopItemModel({required this.category});

  factory ShopItemModel.fromJson(Map<String, dynamic> json) {
    return ShopItemModel(
      category: List<CategoryList>.from(
          json["category"].map((x) => CategoryList.fromJson(x))),
    );
  }
}

class CategoryList {
  String name;
  List<FoodsItem> hotFood;
  List<FoodsItem> soup;
  List<FoodsItem> burgers;
  List<FoodsItem> pizza;
  List<FoodsItem> desert;

  CategoryList(
      {required this.name,
      required this.hotFood,
      required this.soup,
      required this.burgers,
      required this.pizza,
      required this.desert});

  factory CategoryList.fromJson(Map<String, dynamic> json) {
    return CategoryList(
      name: json['name'],
      hotFood: List<FoodsItem>.from(
          json["hotFood"].map((x) => FoodsItem.fromJson(x))),
      soup:
          List<FoodsItem>.from(json["soup"].map((x) => FoodsItem.fromJson(x))),
      burgers: List<FoodsItem>.from(
          json["burgers"].map((x) => FoodsItem.fromJson(x))),
      pizza:
          List<FoodsItem>.from(json["pizza"].map((x) => FoodsItem.fromJson(x))),
      desert: List<FoodsItem>.from(
          json["desert"].map((x) => FoodsItem.fromJson(x))),
    );
  }
}

class FoodsItem {
  FoodsItem(
      {required this.id,
      required this.name,
      required this.price,
      required this.duration,
      required this.image,
      required this.description,
      required this.kcal,
      required this.gram,
      required this.proteins,
      required this.fats,
      required this.carbohydrates,
      required this.shopId});

  String name;
  String price;
  String duration;
  String image;
  String description;
  String kcal;
  String gram;
  String proteins;
  String fats;
  String carbohydrates;
  int id;
  int? shopId;

  factory FoodsItem.fromJson(Map<String, dynamic> json) {
    return FoodsItem(
      name: json['name'],
      price: json['price'],
      duration: json['duration'],
      description: json['description'],
      kcal: json['kcal'],
      gram: json['gram'],
      proteins: json['proteins'],
      fats: json['fats'],
      carbohydrates: json['carbohydrates'],
      image: json['image'],
      id: json['id'],
      shopId: json['shop_id'] ?? 0,
    );
  }
}
